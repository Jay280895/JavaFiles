package methods_PKG_Learn_Vern;

public class Add
{
	//Addition
	public static int Sum(int A,int B)
	{
		int sum = A+B;
		return sum;
	}
	
	//Division
	public static int Div(int A, int B)
	{
		int Div   = A/B;
		return Div;
	}
	
	
	//Multiplication
	public static int Multi(int A, int B)
	{
		int Multi = A*B;
		return Multi;
		
	}
	
	
	//Subtraction
	public static int Sub(int A, int B)
	{
	
		int Sub  = A-B;
		return Sub;
		
	}

}
